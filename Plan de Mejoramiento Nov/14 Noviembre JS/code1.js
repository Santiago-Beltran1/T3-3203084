//Suma de Dos Números con Conversión

// Solicitar números al usuario
const prompt = require('prompt-sync') ();
let Santiago1 = prompt("Ingrese el primer número:");
let Santiago2 = prompt("Ingrese el segundo número:");

// Convertir a enteros
let Beltran1 = parseInt(Santiago2);
let Beltran2 = parseInt(Santiago1);

// Calcular suma
let SantiagoSum = Beltran1 + Beltran2;

// Mostrar resultado
console.log("La suma es= " + SantiagoSum);